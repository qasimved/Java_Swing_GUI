    private void menuitemsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuitemsearchActionPerformed
        // TODO add your handling code here:
        String brandName = JOptionPane.showInputDialog("Please enter a brand such as Tooheys.");
        if(brandName!=null && brandName.length()>0){
            Liquor liquor = searchBrand(brandName);
            if(liquor!=null){
                selectedLiquorList.clear();
                selectedLiquorList.add(liquor);
                textAreaOutput.setText(formatOutput(selectedLiquorList));
            }
            else{
                JOptionPane.showMessageDialog(null,"No item found. Please see the list of all items");
            }
        }
        
    }//GEN-LAST:event_menuitemsearchActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if(selectedLiquorList.isEmpty()){
            JOptionPane.showMessageDialog(null,"No item selected for order");
        }
        else{
             
        double totalItemPrice = getTotalPrice(selectedLiquorList) * Double.valueOf(String.valueOf(comboBoxQuantity.getSelectedItem()));
        if(RadioMember.isSelected()){
            totalItemPrice = totalItemPrice - (.1 * totalItemPrice);
        }
        
        String output = "Your order is "+ String.valueOf(comboBoxQuantity.getSelectedItem())+ "carton(s) of "+selectedLiquorList.get(0).brand+ "\nTotal price: "+totalItemPrice;
            JOptionPane.showMessageDialog(null,output);
        }
       
    }//GEN-LAST:event_jButton1ActionPerformed

