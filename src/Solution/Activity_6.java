    private double getTotalPrice(ArrayList<Liquor> selectedLiquorList){
        double total=0;
        for(Liquor liquor: selectedLiquorList){
            total = total+liquor.price;
        }
        return total;
    }
    
    private void reset(){
        String output="---------------------------------------------------------------------------------------------------------- \n";
        output = output + String.format(" %10s %50s %50s","Brand","Price","YearBrewed");
        output = output + "\n----------------------------------------------------------------------------------------------------------\n";
        textAreaOutput.setText(output);
        RadioNonMember.setSelected(true);
        comboBoxQuantity.setSelectedIndex(0);
         
    }
    
    private Liquor searchBrand(String brandName){
        for(Liquor liquor: liquorList){
            if(liquor.brand.trim().equalsIgnoreCase(brandName.trim())){
                return liquor;
            }
        }
        return null;
    }
    
    private void init(){
        String output="---------------------------------------------------------------------------------------------------------- \n";
        output = output + String.format(" %10s %50s %50s","Brand","Price","YearBrewed");
        output = output + "\n----------------------------------------------------------------------------------------------------------\n";
        textAreaOutput.setText(output);
       // textAreaOutput.setEnabled(false);
        liquorList = new ArrayList<Liquor>();
        liquorList.add(new Liquor("Asahi",30,"2001")); 
        liquorList.add(new Liquor("Yanjing",27,"2002")); 
        liquorList.add(new Liquor("Tooheys",33,"2000")); 
        liquorList.add(new Liquor("Heinek",35,"2004")); 
        selectedLiquorList = new ArrayList<Liquor>();
        
    }
    

    
    public class Liquor {
        public String brand = "";
        public double price = 0;
        public String yearBrewed = "";
        
        public Liquor(String brand, double price, String yearBrewed){
            this.brand = brand;
            this.price = price;
            this.yearBrewed = yearBrewed;
        }
    }
    
    private String formatOutput(ArrayList<Liquor> selectedLiquorList){
          String output="---------------------------------------------------------------------------------------------------------- \n";
        output = output + String.format(" %10s %50s %50s","Brand","Price","YearBrewed");
        output = output + "\n----------------------------------------------------------------------------------------------------------\n";
        for(Liquor liquor: selectedLiquorList){
            String formatedStr = String.format(" %10s %50s %50s",liquor.brand,liquor.price,liquor.yearBrewed);
               output= output +formatedStr+"\n";
        }
        return output;
    }
    
    

